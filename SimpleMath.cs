namespace SimpleLibrary
{

    public static class SimpleMath
    {

        public static float Add(float factor1, float factor2)
        {
            return factor1 + factor2;
        }

        public static float Substract(float factor1, float factor2)
        {
            return factor1 - factor2;
        }

        public static float Multiply(float factor1, float factor2)
        {
            return factor1 * factor2;
        }

        public static float Divide(float factor1, float factor2)
        {
            return factor1 / factor2;
        }

    }

}
